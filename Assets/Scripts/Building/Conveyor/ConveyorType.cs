public enum ConveyorType
{
    Straight,       // Прямой (1 вход, 1 выход)
    Corner,         // Поворот (1 вход, 1 выход, 90°)
    Splitter,       // Разветвитель (1 вход, N выходов)
    Junction        // Перекресток (N входов, N выходов)
    
    // Пространство для расширения:
    // Merger,      // Объединитель (N входов, 1 выход)
    // Balancer,    // Балансировщик (N входов, N выходов с равномерным распределением)
    // Filter,      // Фильтр (пропускает только определенные ресурсы)
}